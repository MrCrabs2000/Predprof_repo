'''Создание и базовые операции
Path() - создает объект пути

/ - объединяет части пути (оператор)

Path.cwd() - текущая рабочая директория

Path.home() - домашняя директория пользователя

Свойства пути
.name - имя файла с расширением

.stem - имя файла без расширения

.suffix - расширение файла

.parent - родительская директория

.parents - список всех родительских директорий

.parts - кортеж всех частей пути

.anchor - корневая часть пути (/ или C:)

Проверки
.exists() - существует ли путь

.is_file() - является ли файлом

.is_dir() - является ли папкой

.is_absolute() - абсолютный ли путь

.is_symlink() - является ли ссылкой

Работа с файлами
.read_text() - читает текст из файла

.write_text() - записывает текст в файл

.read_bytes() - читает байты из файла

.write_bytes() - записывает байты в файл

.touch() - создает пустой файл

.unlink() - удаляет файл

.rename() - переименовывает файл

.replace() - перемещает/заменяет файл

Работа с папками
.mkdir() - создает папку

.rmdir() - удаляет пустую папку

.iterdir() - просматривает содержимое папки

.glob() - ищет файлы по шаблону

.rglob() - рекурсивный поиск по шаблону

Информация о файле
.stat().st_size - размер файла в байтах

.stat().st_mtime - время последнего изменения

.owner() - владелец файла

.group() - группа файла

Преобразования
.absolute() - абсолютный путь

.resolve() - нормализованный абсолютный путь

.relative_to() - относительный путь от указанного

.with_name() - заменяет имя файла

.with_suffix() - заменяет расширение'''


